package dayOne;

public class Data_Types {

	public static void main(String[] args) {
		// Boolean
		boolean a = true;
		char b = '&';
		
		
		//Integer
		byte c = 127 ;
		short d = 32767;
		int e= 1000000000;
		long l = 1000000000L;
		
		//Casting
		long lg = c;
		System.out.println(lg);
		byte bt = (byte) 10000000;
		System.out.println(bt);
		
		//Floating
		float f = 10.00f;
		double g = 10.00d;
		
		
		byte b1 = 127;
		byte b2 = 127;
		short st = (short) (b1+b2);
		System.out.println(st);
		
		

	}

}
