package Java_Day_1;

public class static_keyword_3 {
	int a;
	static int b;

	public static void main(String[] args) {

		static_keyword_3 obj = new static_keyword_3();
		obj.changeValue(10, 20);
		obj.displayValue();

		System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");

		static_keyword_3 obj1 = new static_keyword_3();
		obj1.displayValue();

		System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");

		static_keyword_3 obj2 = new static_keyword_3();
		obj2.changeValue(20, 40);
		obj2.displayValue();

		System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
		obj1.displayValue();

		System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
		obj.displayValue();
		
		System.out.println(b);
		System.out.println(obj.a);
	}

	public void changeValue(int c, int d) {
		a = c;
		b = d;
		
	}

	public void displayValue() {
		System.out.println(a);
		System.out.println(b);
	}

}
