package day_one;

public class Condition_Statement {

	public static void main(String[] args) {
		byte n = 100;

		if (n < 100) {
			System.out.println("Number is less than 100 ");
		} else if (n == 100) {
			System.out.println("Number is equal to 100");
		} else
			System.out.println("Number is greater than 100");

	}

}
